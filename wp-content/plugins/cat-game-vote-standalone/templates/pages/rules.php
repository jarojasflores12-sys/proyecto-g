<section class="cg-rules-page" aria-labelledby="cg-rules-title">
    <header class="cg-card cg-rules-hero">
        <h2 id="cg-rules-title">Reglas de la comunidad PetUnity</h2>
        <p>Estas reglas aplican a todo el ecosistema del juego y buscan proteger una experiencia segura, justa y divertida para todas las personas y mascotas.</p>
    </header>

    <section class="cg-card cg-rules-section">
        <h3>1. Uso de la plataforma</h3>
        <p>Usa PetUnity de forma responsable: comparte contenido propio o autorizado, respeta a otros usuarios y evita suplantaciones, spam o actividades engañosas.</p>
        <p>Cada cuenta es responsable de lo que publica y de mantener una conducta acorde al espíritu de comunidad.</p>
    </section>

    <section class="cg-card cg-rules-section">
        <h3>2. La Arena (modo competitivo)</h3>
        <p>La Arena está diseñada para competir de forma transparente y amistosa. Todas las publicaciones en este modo participan por posiciones visibles.</p>
        <p>No se permite manipular resultados mediante prácticas artificiales o cualquier conducta que altere la competencia.</p>
    </section>

    <section class="cg-card cg-rules-section">
        <h3>3. El Parque (modo libre)</h3>
        <p>El Parque es un espacio social para compartir sin presión de competencia directa. Aquí se prioriza la convivencia y el descubrimiento de contenido.</p>
        <p>Aun siendo modo libre, siguen aplicando las normas de respeto, seguridad y bienestar animal.</p>
    </section>

    <section class="cg-card cg-rules-section">
        <h3>4. Sistema de puntuación</h3>
        <p>La puntuación depende de las reacciones registradas por la comunidad conforme al sistema activo de la plataforma.</p>
        <p>Los criterios visibles en pantalla son la referencia oficial para el cálculo de posiciones.</p>
    </section>

    <section class="cg-card cg-rules-section">
        <h3>5. Desempate en ranking</h3>
        <p>Cuando dos o más publicaciones empatan, se aplican criterios de desempate internos para mantener un orden consistente en el ranking.</p>
        <p>El orden final mostrado por la plataforma será el válido para efectos de resultados y reconocimientos.</p>
    </section>

    <section class="cg-card cg-rules-section">
        <h3>6. Moderación de contenido</h3>
        <p>No se permiten contenidos ofensivos, violentos, discriminatorios o que vulneren a personas o animales.</p>
        <p>El equipo de PetUnity puede revisar, ocultar o retirar publicaciones que incumplan estas reglas.</p>
    </section>

    <section class="cg-card cg-rules-section">
        <h3>7. Sistema de sanciones</h3>
        <p>Según gravedad y reincidencia, pueden aplicarse advertencias, ajustes de puntos y bloqueos temporales de participación.</p>
        <p>Las medidas se aplican para proteger el juego limpio y la seguridad de la comunidad.</p>
    </section>

    <section class="cg-card cg-rules-section">
        <h3>8. Reportes falsos</h3>
        <p>Los reportes deben realizarse de buena fe y con fundamento. Usar esta herramienta para dañar a otros usuarios se considera abuso.</p>
        <p>Los reportes malintencionados pueden derivar en acciones correctivas sobre la cuenta reportante.</p>
    </section>

    <section class="cg-card cg-rules-section">
        <h3>9. Apelaciones</h3>
        <p>Si una publicación es moderada o eliminada, el usuario puede consultar el motivo y presentar apelación cuando el flujo lo permita.</p>
        <p>Las apelaciones se revisan bajo los mismos criterios de seguridad, evidencia y cumplimiento de normas.</p>
    </section>

    <section class="cg-card cg-rules-section">
        <h3>10. Publicaciones de adopción</h3>
        <p>Las publicaciones de adopción deben ser veraces, claras y orientadas al bienestar animal.</p>
        <p>No se admiten publicaciones engañosas o que promuevan prácticas que pongan en riesgo a las mascotas.</p>
    </section>

    <section class="cg-card cg-rules-section">
        <h3>11. Reconocimiento a ganadores</h3>
        <p>Los ganadores se determinan conforme al ranking final y a las reglas vigentes del evento correspondiente.</p>
        <p>Cualquier reconocimiento puede quedar sujeto a verificación de cumplimiento de normas de la comunidad.</p>
    </section>

    <section class="cg-card cg-rules-section">
        <h3>12. Espíritu de la comunidad</h3>
        <p>PetUnity promueve respeto, empatía, juego limpio y amor por los animales.</p>
        <p>Participar con buena intención fortalece la comunidad y mejora la experiencia para todos.</p>
    </section>

