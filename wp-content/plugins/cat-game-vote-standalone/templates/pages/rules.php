<section class="cg-rules-page" aria-labelledby="cg-rules-title">
    <header class="cg-card cg-rules-hero">
        <h2 id="cg-rules-title">Reglas de la comunidad PetUnity</h2>
        <p>PetUnity permite compartir fotos de mascotas o animales, participar en competencias amistosas y ayudar a animales que necesitan hogar.</p>
    </header>

    <section class="cg-card cg-rules-section">
        <h3>1. Uso de la plataforma</h3>
        <p>En la plataforma existen dos formas principales de publicar:</p>
        <p><strong>🏆 La Arena (modo competitivo)</strong></p>
        <p>La Arena es el modo competitivo de PetUnity.</p>
        <p>Los usuarios pueden participar en eventos temáticos.</p>
        <p>Las publicaciones compiten entre sí mediante las reacciones de la comunidad.</p>
        <p>Mientras más reacciones reciba una publicación, mayor será su posición en el ranking.</p>
        <p>Al finalizar el evento se anuncian los ganadores.</p>
        <p><strong>🌳 El Parque (modo libre)</strong></p>
        <p>El Parque es un espacio para compartir fotos sin competir.</p>
        <p>Aquí puedes publicar libremente.</p>
        <p>Las publicaciones también pueden recibir reacciones de la comunidad.</p>
        <p>Estas reacciones no afectan el ranking competitivo.</p>
        <p>Es ideal para compartir momentos, historias o animales sin participar en eventos.</p>
    </section>

    <section class="cg-card cg-rules-section">
        <h3>2. Publicaciones permitidas</h3>
        <p>Las publicaciones deben cumplir con las siguientes condiciones:</p>
        <p>Las imágenes deben mostrar mascotas o animales.</p>
        <p>Las fotos deben ser reales y respetuosas.</p>
        <p>Puedes publicar mascotas propias o animales que estés ayudando.</p>
        <p>No se permite publicar contenido que:</p>
        <ul>
            <li>muestre violencia o maltrato animal</li>
            <li>sea ofensivo o degradante</li>
            <li>vulnere a otras personas</li>
            <li>contenga imágenes inapropiadas</li>
            <li>no esté relacionado con animales</li>
            <li>sea spam o publicación repetida de forma abusiva</li>
        </ul>
        <p>Las publicaciones que incumplan estas normas pueden ser moderadas o eliminadas.</p>
    </section>

    <section class="cg-card cg-rules-section">
        <h3>3. Sistema de puntuación</h3>
        <p>Las publicaciones reciben reacciones de la comunidad.</p>
        <p>Cada reacción suma puntos a la publicación.</p>
        <p>Todas las reacciones tienen el mismo valor.</p>
        <p>Mientras más reacciones reciba una publicación, mayor será su posición en el ranking del evento.</p>
    </section>

    <section class="cg-card cg-rules-section">
        <h3>4. Desempate en el ranking</h3>
        <p>En caso de empate entre publicaciones con la misma cantidad de puntos:</p>
        <p>ganará la publicación que haya alcanzado ese puntaje primero</p>
        <p>Este sistema permite mantener un ranking claro y justo.</p>
    </section>

    <section class="cg-card cg-rules-section">
        <h3>5. Moderación de contenido</h3>
        <p>PetUnity cuenta con un sistema de moderación para mantener la comunidad segura.</p>
        <p>La moderación puede ocurrir de dos formas:</p>
        <p><strong>Moderación por administradores</strong></p>
        <p>Los administradores pueden revisar publicaciones y tomar acciones cuando sea necesario.</p>
        <p><strong>Moderación por la comunidad</strong></p>
        <p>Los usuarios pueden reportar publicaciones que consideren inapropiadas.</p>
        <p>Cada reporte será revisado antes de tomar una decisión.</p>
        <p>Si una publicación es reportada, el usuario podrá ver:</p>
        <ul>
            <li>que su publicación fue reportada</li>
            <li>el motivo del reporte</li>
            <li>el resultado de la revisión</li>
            <li>la opción de apelar si no está conforme con la decisión</li>
        </ul>
    </section>

    <section class="cg-card cg-rules-section">
        <h3>6. Sistema de sanciones</h3>
        <p>Para mantener una comunidad justa y respetuosa, PetUnity utiliza un sistema de puntos de infracción.</p>
        <p>Los puntos representan penalizaciones por incumplimiento de las normas.</p>
        <p>Cada infracción suma puntos al usuario.</p>
        <p>Mientras más puntos acumule un usuario, mayores serán las sanciones aplicadas.</p>
        <p>Los puntos acumulados expiran después de 1 año desde que fueron asignados.</p>
        <p><strong>Infracciones leves</strong></p>
        <p>Las infracciones leves suman 1 punto.</p>
        <p>Ejemplos:</p>
        <ul>
            <li>subir fotos repetidas</li>
            <li>subir imágenes que no corresponden al objetivo de la plataforma</li>
            <li>publicar contenido que no muestra animales de forma adecuada</li>
        </ul>
        <p>Ejemplo de acumulación:</p>
        <p>Un usuario sube una foto de un animal que no corresponde a una mascota doméstica.</p>
        <p>Luego sube una foto repetida.</p>
        <p>Ahora el usuario tiene 2 puntos acumulados.</p>
        <p>Posteriormente sube una foto de un paisaje.</p>
        <p>El usuario alcanza 3 puntos por infracciones leves.</p>
        <p>Sanción por acumulación de 3 puntos leves:</p>
        <ul>
            <li>no podrá subir nuevas publicaciones durante algunos días</li>
            <li>pero podrá seguir reaccionando a otras publicaciones</li>
        </ul>
        <p>Si el usuario vuelve a acumular 3 puntos adicionales por infracciones leves, la sanción pasará a considerarse infracción moderada.</p>
        <p><strong>Infracciones moderadas</strong></p>
        <p>Las infracciones moderadas pueden ocurrir cuando:</p>
        <ul>
            <li>un usuario acumula reiteradamente infracciones leves</li>
            <li>ignora advertencias del sistema</li>
            <li>continúa publicando contenido inapropiado para la comunidad</li>
        </ul>
        <p>Sanción moderada:</p>
        <ul>
            <li>bloqueo temporal para subir publicaciones</li>
            <li>bloqueo temporal para reaccionar</li>
            <li>acumulación adicional de puntos de infracción</li>
        </ul>
        <p>La duración de estas restricciones dependerá de la gravedad y reincidencia del comportamiento.</p>
        <p><strong>Infracciones graves</strong></p>
        <p>Las infracciones graves corresponden a conductas que afectan seriamente a la comunidad.</p>
        <p>Ejemplos incluyen:</p>
        <ul>
            <li>contenido ofensivo o degradante</li>
            <li>imágenes violentas o inapropiadas</li>
            <li>uso abusivo de la plataforma</li>
        </ul>
        <p>Las sanciones graves pueden incluir:</p>
        <ul>
            <li>eliminación inmediata del contenido</li>
            <li>bloqueo temporal de la cuenta</li>
            <li>restricciones prolongadas dentro de la plataforma</li>
        </ul>
        <p>En casos repetidos o especialmente graves, la cuenta puede ser suspendida.</p>
    </section>

    <section class="cg-card cg-rules-section">
        <h3>7. Reportes falsos</h3>
        <p>El sistema de reportes existe para ayudar a mantener la comunidad segura.</p>
        <p>Para evitar abusos, los reportes falsos también generan sanciones.</p>
        <p>Se considera reporte falso cuando un usuario reporta contenido que claramente no infringe las normas y, tras la revisión, el administrador lo marca como falso.</p>
        <p>Las sanciones se aplican de forma progresiva:</p>
        <ul>
            <li>al acumular 3 reportes falsos, el usuario no podrá realizar nuevos reportes durante 3 días</li>
            <li>si vuelve a ocurrir, el bloqueo para reportar será de 7 días</li>
        </ul>
        <p>Si el comportamiento continúa:</p>
        <ul>
            <li>se bloqueará la capacidad de reportar durante 7 días</li>
            <li>además no podrá subir publicaciones durante 3 días</li>
        </ul>
        <p>Si el abuso continúa nuevamente:</p>
        <ul>
            <li>se bloqueará la capacidad de reportar y subir publicaciones durante 7 días</li>
        </ul>
        <p>Si un usuario acumula 5 o más episodios de abuso del sistema de reportes en un plazo de 1 mes, su cuenta podrá ser bloqueada o suspendida para proteger a la comunidad.</p>
        <p>Estas medidas buscan asegurar que el sistema de reportes se utilice de forma responsable.</p>
    </section>

    <section class="cg-card cg-rules-section">
        <h3>8. Apelaciones</h3>
        <p>Si una publicación es moderada o eliminada, el usuario podrá:</p>
        <ul>
            <li>ver el motivo de la decisión</li>
            <li>solicitar una revisión si considera que hubo un error</li>
        </ul>
        <p>Los plazos para presentar una apelación son:</p>
        <ul>
            <li>72 horas para sanciones leves o moderadas</li>
            <li>24 horas para sanciones graves</li>
        </ul>
        <p>Las apelaciones serán revisadas por el equipo de moderación.</p>
        <p>Una vez evaluada la apelación, el usuario recibirá una notificación con el resultado.</p>
    </section>

    <section class="cg-card cg-rules-section">
        <h3>9. Publicaciones de adopción</h3>
        <p>La sección de adopciones permite ayudar a animales que necesitan un hogar.</p>
        <p>Las publicaciones deben cumplir las siguientes condiciones:</p>
        <ul>
            <li>no se permite utilizar esta sección para venta de animales</li>
            <li>solo debe utilizarse para adopción o búsqueda de hogar temporal</li>
            <li>una publicación puede indicar adopción, hogar temporal o ambas opciones si corresponde</li>
        </ul>
        <p>Si un animal está herido o en tratamiento:</p>
        <ul>
            <li>las heridas no deben mostrarse en la imagen</li>
            <li>no se permiten fotos con sangre o lesiones visibles</li>
            <li>las imágenes deben ser respetuosas con el animal</li>
        </ul>
        <p>Esto permite mantener un espacio seguro para todos los usuarios.</p>
    </section>

    <section class="cg-card cg-rules-section">
        <h3>10. Reconocimiento a los ganadores</h3>
        <p>Los ganadores de los eventos en La Arena reciben reconocimiento dentro de la plataforma.</p>
        <p>Esto incluye:</p>
        <ul>
            <li>una insignia especial en su perfil</li>
            <li>aparecer en el historial de ganadores</li>
            <li>la posibilidad de que su perfil o publicación sea destacado temporalmente dentro de la comunidad</li>
        </ul>
        <p>Estos reconocimientos celebran la participación y el cariño por los animales.</p>
    </section>

    <section class="cg-card cg-rules-section">
        <h3>11. Espíritu de la comunidad</h3>
        <p>PetUnity busca ser una comunidad positiva donde los animales sean los protagonistas.</p>
        <p>Se espera que todos los usuarios participen con respeto, empatía y amor por los animales.</p>
    </section>

